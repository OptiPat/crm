/** Annexes SCPI — § 3 Modalités d'acquisition (démembrement). */

export const ANNEXES_SCPI_SECTION3_MODALITES_BODY = `[u]3. Modalités d'acquisition[/u]

Les parts de SCPI peuvent être acquises en pleine propriété ou en démembrement de propriété.
Ce démembrement peut être temporaire ou viager.

[u]Acquisition en démembrement de propriété[/u]

Le démembrement temporaire de propriété est une modalité particulière de souscription qui permet de scinder le droit de propriété entre :
- un nu-propriétaire, détenteur de la nue-propriété, c'est-à-dire du droit de disposer du bien (abusus) ;
- un usufruitier, détenteur de l'usufruit, c'est-à-dire des droits d'usage (usus) et de perception des revenus (fructus).

Les deux parties se partagent les prérogatives liées au droit de propriété des parts de SCPI (nue-propriété/usufruit) pour une période de temps fixe (la durée du démembrement de propriété), déterminée à l'avance. Cette durée est en principe comprise entre 3 et 20 ans.

Pendant la durée du démembrement :
- le nu-propriétaire détient la nue-propriété des parts de la SCPI mais ne perçoit aucun revenu ;
- l'usufruitier est le seul bénéficiaire des revenus, notamment les dividendes, que les parts de SCPI peuvent produire.

En contrepartie, l'usufruitier est le seul, sur la durée du démembrement, à payer les coûts indirects.

Les prix payés par l'usufruitier et le nu-propriétaire sont inférieurs à la valeur de la part en pleine propriété et résultent d'une clé de répartition retenue en fonction de la durée du démembrement prévue, calculée en principe selon une valorisation économique. Les frais de souscription sont payés par les deux contreparties, au prorata de la clé de répartition. Cette offre est éligible aux personnes physiques comme aux personnes morales.

Le nu-propriétaire est ainsi généralement un contribuable disposant de revenus et d'un patrimoine importants – souvent redevable de l'impôt sur la fortune immobilière (ci-après « IFI ») – souhaitant se constituer de futurs revenus complémentaires.

À ce titre, une souscription de parts de SCPI en nue-propriété peut notamment présenter les intérêts suivants :
- se constituer un patrimoine (en fonds propres ou par endettement) en ne payant qu'une quote-part du prix de la pleine propriété ;
- ne pas être taxé à l'extinction du démembrement au moment de la reconstitution de la pleine propriété ;
- ne pas être assujetti à l'IFI au titre de cet investissement pendant la durée du démembrement.

À contrario, l'usufruitier est généralement un contribuable souhaitant percevoir des revenus complémentaires avec un investissement de départ réduit, ou un investisseur institutionnel souhaitant effectuer un placement de trésorerie. À ce titre, une souscription de parts de SCPI en usufruit peut notamment présenter les intérêts suivants :
- percevoir des revenus réguliers sur une période fixée ;
- percevoir des revenus sur la valeur de la pleine propriété en contrepartie d'une souscription à un prix décoté par rapport à celui de la pleine propriété.`;
